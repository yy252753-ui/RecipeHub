async function main() {
  for (const fix of fixes) {
    await prisma.ingredient.deleteMany({ where: { recipeId: fix.id } });
    await prisma.step.deleteMany({ where: { recipeId: fix.id } });

    await prisma.recipe.update({
      where: { id: fix.id },
      data: { description: fix.description }
    });

    await prisma.ingredient.createMany({
      data: fix.ingredients.map(([name, amount, unit], index) => ({
        recipeId: fix.id,
        name,
        amount,
        unit,
        order: index + 1
      }))
    });

    await prisma.step.createMany({
      data: fix.steps.map((description, index) => ({
        recipeId: fix.id,
        description,
        order: index + 1
      }))
    });

    console.log(`✓ ${fix.id}`);
  }

  console.log(`\n총 ${fixes.length}개 레시피 수정 완료`);
}

main()
  .then(() => prisma.$disconnect())
  .catch(async (error) => {
    console.error(error);
    await prisma.$disconnect();
    process.exit(1);
  });
